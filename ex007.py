# Desenvolva um programa que leia as duas notas de um aluno, calcule e mostre sua média.

n1 = float(input('Digite a uma nota: '))
n2 = float(input('Digite a outra nota: '))
med = (n1 + n2) / 2
print(f'A média das notas é {med}.')

